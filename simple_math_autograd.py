print("-" * 50)
print("🌟 Это САМАЯ ВАЖНАЯ ФУНКЦИЯ PyTorch!")
print("   Она позволяет нейросетям учиться на своих ошибках\n")

# --------------------------------------------------------------------
# Пример 1: Простая функция y = x²
# --------------------------------------------------------------------
print("📌 Пример 1: Функция y = x²")
print("   (Показывает, как меняется y при изменении x)")

# Создаем переменную x с отслеживанием градиента
x = torch.tensor(3.0, requires_grad=True)
print(f"\n   x = {x}")

# Вычисляем y = x²
y = x ** 2
print(f"   y = x² = {y}")

# Вычисляем градиент (производную)
y.backward()

# Производная x² = 2x, при x=3 должно быть 6
print(f"\n   📈 dy/dx = {x.grad}")
print(f"   Объяснение: производная x² = 2x, при x=3: 2×3 = {x.grad}")

# --------------------------------------------------------------------
# Пример 2: Функция с несколькими операциями
# --------------------------------------------------------------------
print("\n📌 Пример 2: Более сложная функция y = x³ + 2x")

# Обнуляем градиент и создаем новую переменную
x = torch.tensor(2.0, requires_grad=True)
print(f"\n   x = {x}")

# y = x³ + 2x
y = x**3 + 2*x
print(f"   y = x³ + 2x = {y}")

y.backward()

# Производная: 3x² + 2, при x=2: 3×4 + 2 = 12 + 2 = 14
print(f"\n   📈 dy/dx = {x.grad}")
print(f"   Объяснение: производная = 3x² + 2, при x=2: 3×4 + 2 = {x.grad}")

# --------------------------------------------------------------------
# Пример 3: Функция от двух переменных
# --------------------------------------------------------------------
print("\n📌 Пример 3: Функция от двух переменных f(a,b) = a² + b²")

# Создаем две переменные
a = torch.tensor(3.0, requires_grad=True)
b = torch.tensor(4.0, requires_grad=True)

print(f"\n   a = {a}, b = {b}")

# f = a² + b²
f = a**2 + b**2
print(f"   f = a² + b² = {f}")

# Вычисляем градиенты
f.backward()

print(f"\n   📈 ∂f/∂a = {a.grad}  (производная по a: 2a = 2×3 = 6)")
print(f"   📈 ∂f/∂b = {b.grad}  (производная по b: 2b = 2×4 = 8)")

# --------------------------------------------------------------------
# Пример 4: Как нейросеть учится (упрощенно)
# --------------------------------------------------------------------
print("\n📌 Пример 4: Как нейросеть учится (простая аналогия)")

# Представьте, что это вес нейрона
weight = torch.tensor(2.0, requires_grad=True)

# Правильный ответ
target = 5.0

# Функция ошибки (чем меньше, тем лучше)
def error(weight):
    prediction = weight * 3  # простая модель
    return (prediction - target) ** 2

print(f"\n   Начальный вес: {weight}")
print(f"   Целевое значение: {target}")

# Делаем несколько шагов обучения
learning_rate = 0.1

for step in range(5):
    # Вычисляем ошибку
    loss = error(weight)
    
    # Вычисляем градиент
    loss.backward()
    
    # Обновляем вес (двигаемся против градиента)
    with torch.no_grad():
        weight -= learning_rate * weight.grad
    
    print(f"\n   Шаг {step + 1}:")
    print(f"     Ошибка: {loss.item():.3f}")
    print(f"     Градиент: {weight.grad.item():.3f}")
    print(f"     Новый вес: {weight.item():.3f}")
    
    # Обнуляем градиент для следующего шага
    weight.grad.zero_()

print(f"\n   ✅ Вес изменился, чтобы уменьшить ошибку!")

# --------------------------------------------------------------------
# Пример 5: Графическое понимание градиента
