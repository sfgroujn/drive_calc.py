# ============================================================================
# ЧАСТЬ 6: ПРАКТИЧЕСКИЙ ПРИМЕР - МИНИМИЗАЦИЯ ФУНКЦИИ
# ============================================================================
print("\n🎯 ЧАСТЬ 6: Практический пример - поиск минимума функции")
print("-" * 50)
print("Задача: найти x, который минимизирует f(x) = (x - 3)²")
print("Ожидаемый ответ: x = 3\n")

# Начинаем с x = 0
x = torch.tensor(0.0, requires_grad=True)

print("Процесс поиска минимума:")
print("   x  |  f(x)  |  градиент  |  действие")
print("-" * 45)

for step in range(10):
    # Вычисляем функцию
    f = (x - 3) ** 2
    
    # Сохраняем текущее значение
    current_x = x.item()
    current_f = f.item()
    
    # Вычисляем градиент
    f.backward()
    gradient = x.grad.item()
    
    # Обновляем x (двигаемся против градиента)
    with torch.no_grad():
        x -= 0.5 * gradient  # learning_rate = 0.5
    
    print(f"   {current_x:.3f} | {current_f:.3f} | {gradient:+.3f}    | x = x - 0.5×{gradient:.3f} = {x.item():.3f}")
    
    # Обнуляем градиент
    x.grad.zero_()

print(f"\n✅ Найден минимум: x = {x.item():.3f} (должно быть 3.0)")

