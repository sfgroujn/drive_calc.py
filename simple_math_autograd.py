print("   ├───────┼───────┼───────┼───────┼───────┤")
print("   │ dy/dx │   2   │   4   │   6   │   8   │")
print("   └───────┴───────┴───────┴───────┴───────┘")
print("\n   📖 Чем больше x, тем быстрее растет y")
